'use client'

/**
 * POS Product Grid Component
 * Displays products in a grid with search functionality
 */

import Image from 'next/image'
import { useCartStore } from '@/lib/store/cart-store'
import { Input } from '@/components/ui/input'
import { Button } from '@/components/ui/button'
import { Card } from '@/components/ui/card'
import { Search, Plus, Package } from 'lucide-react'
import { formatCurrency } from '@/lib/store/cart-store'

interface Product {
  id: string
  sku: string
  name: string
  price: number
  barcode: string | null
  imageUrl: string | null
  category: { id: string; name: string } | null
  inventoryId: string
  quantity: number
}

interface POSProductGridProps {
  products: Product[]
  searchQuery: string
  onSearchChange: (query: string) => void
}

export function POSProductGrid({
  products,
  searchQuery,
  onSearchChange,
}: POSProductGridProps) {
  const addItem = useCartStore((state) => state.addItem)

  const handleAddToCart = (product: Product) => {
    if (product.quantity <= 0) {
      // Show toast or alert that product is out of stock
      return
    }

    addItem({
      productId: product.id,
      inventoryId: product.inventoryId,
      name: product.name,
      sku: product.sku,
      barcode: product.barcode,
      price: product.price,
      maxStock: product.quantity,
    })
  }

  return (
    <div className="flex flex-col flex-1 overflow-hidden">
      {/* Search Bar */}
      <div className="pb-4">
        <div className="relative">
          <Search className="absolute left-3 top-1/2 h-4 w-4 -translate-y-1/2 text-gray-400" />
          <Input
            type="text"
            placeholder="Search by name, SKU, or barcode..."
            value={searchQuery}
            onChange={(e) => onSearchChange(e.target.value)}
            className="pl-10"
            autoFocus
          />
        </div>
      </div>

      {/* Product Grid */}
      <div className="flex-1 overflow-auto">
        {products.length === 0 ? (
          <div className="flex h-full items-center justify-center text-gray-400">
            <div className="text-center">
              <Package className="mx-auto h-12 w-12 mb-2" />
              <p>No products found</p>
              {searchQuery && (
                <p className="text-sm">Try a different search term</p>
              )}
            </div>
          </div>
        ) : (
          <div className="grid grid-cols-2 md:grid-cols-3 lg:grid-cols-4 gap-3 pb-4">
            {products.map((product) => (
              <Card
                key={product.id}
                className="cursor-pointer transition-all hover:shadow-md overflow-hidden"
                onClick={() => handleAddToCart(product)}
              >
                {/* Product Image */}
                <div className="relative h-32 w-full bg-gray-100 flex items-center justify-center">
                  {product.imageUrl ? (
                    <Image
                      src={product.imageUrl}
                      alt={product.name}
                      fill
                      className="object-cover"
                      sizes="(max-width: 768px) 50vw, (max-width: 1024px) 33vw, 25vw"
                    />
                  ) : (
                    <Package className="h-12 w-12 text-gray-300" />
                  )}
                </div>

                <div className="p-3">
                  {/* Product Name */}
                  <h3 className="font-medium text-sm line-clamp-2 mb-1">
                    {product.name}
                  </h3>

                  {/* SKU */}
                  <p className="text-xs text-gray-500 mb-2">
                    {product.sku}
                  </p>

                  {/* Price and Stock */}
                  <div className="flex items-center justify-between">
                    <span className="text-lg font-bold text-blue-600">
                      {formatCurrency(product.price)}
                    </span>
                    <span
                      className={`text-xs ${
                        product.quantity > 10
                          ? 'text-green-600'
                          : product.quantity > 0
                          ? 'text-orange-600'
                          : 'text-red-600'
                      }`}
                    >
                      {product.quantity > 0
                        ? `${product.quantity} in stock`
                        : 'Out of stock'}
                    </span>
                  </div>

                  {/* Add Button */}
                  <Button
                    size="sm"
                    className="w-full mt-2"
                    disabled={product.quantity <= 0}
                    onClick={(e) => {
                      e.stopPropagation()
                      handleAddToCart(product)
                    }}
                  >
                    <Plus className="h-4 w-4 mr-1" />
                    Add
                  </Button>
                </div>
              </Card>
            ))}
          </div>
        )}
      </div>
    </div>
  )
}
